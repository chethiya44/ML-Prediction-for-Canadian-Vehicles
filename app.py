# Function to calculate emissions
def calculate_emissions(engine_size, cylinders, fuel_consumption, make, model, vehicle_class, transmission, fuel_type):
    # Start with intercept
    emission = coefficients['Intercept']
    
    # Add continuous variables contribution
    emission += coefficients['EngineSize'] * engine_size
    emission += coefficients['Cylinders'] * cylinders
    emission += coefficients['Fuel_Consumption_comb'] * fuel_consumption
    
    # Add categorical variables contribution
    make_key = f'Make_{make}'
    if make_key in coefficients:
        emission += coefficients[make_key]
    
    # For model, we need to use the full model name with make prefix
    model_key = model
    if model_key in model_coefficients:
        emission += model_coefficients[model_key]
    
    vehicle_class_key = f'Vehicle Class_{vehicle_class}'
    if vehicle_class_key in coefficients:
        emission += coefficients[vehicle_class_key]
    
    transmission_key = f'Transmission_{transmission}'
    if transmission_key in coefficients:
        emission += coefficients[transmission_key]
    
    fuel_type_key = f'Fuel Type_{fuel_type}'
    if fuel_type_key in coefficients:
        emission += coefficients[fuel_type_key]
    
    return emission

